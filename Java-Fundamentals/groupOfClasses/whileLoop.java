package groupOfClasses;

public class whileLoop
{
    public static void main(String[] args) {

        int count = 0;
        while (count<=20){
            System.out.println("Count is : "+ count);
            count++;
        }
        int counter =29;
        do {
            System.out.println(counter);
            counter++;
        }
        while(counter<=18);
    }
}
